function [dminVar] = GetMaxZPositionClearance(ZPosTVar,AlphaVar,Gamma,ClrObjHeight)

Gamma = Gamma*(pi/180);

% ZPosTVar = [0.1 0.2 0.3 0.8 0.9 1]';

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
for k = 1:size(ZPosTVar,1)
    dminVar(:,k) = (ClrObjHeight - ZPosTVar(k))./tan(AlphaVar-Gamma);
    
    for z = 1:size(dminVar(:,k),1)
        if dminVar(z,k) < 0
            dminVar(z,k) = NaN;
        end
    end
end
